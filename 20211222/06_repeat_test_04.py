# 기본 문제
# 1의 자리가 6인 첫 번째 7의 배수




num = 7
i = 1

while True:
    # num의 1의 자리가 6인가?
    # 1의 자리? => 10으로 나눈 나머지?
    if num % 10 == 6:
        # num의 1의 자리가 6이다! => 하나 발견하면 최초의 적합한 숫자
        break
    
    # 다음 7의 배수로 넘어가자
    i = i + 1
    num = 7 * i
    
# 무한반복을 빠져나옴 => num의 1의 자리가 6이 되었다.
print(num)


