# 코드 앞부분 : 문제에 필요한 변수들 세팅

my_list = [1, 8, 9, 17, 19]

# 총합을 담게 될 변수
result = 0



# 결과를 계산해내기 위한 logic 코드 (for / while / if 등등 주로 활용)

# my_list 의 숫자들을 하나씩 꺼내보자

for num in my_list :
    
    # 꺼내온 숫자들은 result에 더해주자
    result = result + num


# 계산된 결과를 상황에 맞게 출력

# 최종 계산된 result?
print(result)